#只考虑比本身小的不包括本身和１
for i in range(2,101):
	#i = 2,3,4,5,6,7,8,9,10..100
	flag = 0
	for j in range(2,i):
		#  10 % 2  == 0
		#  7  % 2  == 0 不满足
		#  7  % 3  == 0 不满足
		#  7  % 4  == 0 不满足
		#  7  % 5  == 0 不满足
		#  7  % 6  == 0 不满足
		if i % j == 0: 
			flag = 1
			break

	if flag == 0:
		print(i)
		
	





